numbers = list(range(1,1000001))
# using max() function to print maximum number out of list
print(max(numbers))
# using min() function to print minimum number out of the list
print(min(numbers))
# Adding all one million numbers
print(sum(numbers))
print(f"The addition of first one million numbers is {sum(numbers)}")

