# Regular print output
print("python")
## python

# Adding tab to text
print("\tpython")
##      python

# Adding new line to Text
print("\npython\nJavascript")
## Output:
# python
# Javascript

# Adding tab and new line to text
print("Languages:\n\tPython\n\tC\n\tC#\n\tRust\n\tJavascript")

## Output look like this
# Languages:
#         Python
#         C
#         C#
#         Rust
#         Javascript